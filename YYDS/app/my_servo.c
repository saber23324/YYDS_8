void Servo_Pwm(uint16_t yaw,uint16_t pitch0,uint16_t pitch1,uint16_t pitch2,uint16_t paw,uint16_t time)
{
	static uint16_t Servo1=0x4C;
	static uint16_t Servo2=0x91;
	static uint16_t Servo3=0x71;
	static uint16_t Servo4=0x6B;
	static uint16_t Servo5=0xB9;
	while(1)
	{
		if(Servo1==yaw && Servo2==pitch0 && Servo3==pitch1 && Servo4==pitch2 )
			break;
		else
		{
			if(Servo1<yaw)
				Servo1++;
			if(Servo1>yaw)
				Servo1--;
			
			if(Servo2<pitch0)
				Servo2++;
			if(Servo2>pitch0)
				Servo2--;
			
			if(Servo3<pitch1)
				Servo3++;
			if(Servo3>pitch1)
				Servo3--;
			if(Servo4<pitch2)
				Servo4++;
			if(Servo4>pitch2)
				Servo4--;
			
;
			
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, Servo1);//PE14
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, Servo2);//PE13
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, Servo3);//PE11
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, Servo4);//PE9

		}
					HAL_Delay(time);
	}
	while(1)
	{
		if(Servo5==paw)
			break;
		else
		{
			if(Servo5<paw)
				Servo5++;
			if(Servo5>paw)
				Servo5--;
		__HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_2, paw);//PE6
		}
		
		HAL_Delay(5);
	}
	HAL_Delay(5);
}
