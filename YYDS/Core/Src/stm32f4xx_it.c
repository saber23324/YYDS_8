/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */

  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */
  uint8_t tmp_flag =__HAL_UART_GET_FLAG(&huart4,UART_FLAG_IDLE); //获取IDLE状态
  if((tmp_flag != RESET))//判断接收是否结束
    { 
       __HAL_UART_CLEAR_IDLEFLAG(&huart4);//清楚标志位
       HAL_UART_DMAStop(&huart4); 
		uint8_t headH;
		uint8_t headL;

			if (uart_left[0] == 0x00||uart_left[0] == 0x01) // 
			{
				headH = uart_left[1];
				headL = uart_left[2];
								if(headH<=0x02)//某些情况下会错穿  垃圾商家不写帧头
					{

								if(uart_left[0]>0)
				{
					left_bias=(int)(((short)(headH<<8))|headL);
				}
				else
				{
					left_bias=-(int)(((short)(headH<<8))|headL);
				}
				left_bias=Move_LEFT_Min_Filter(left_bias);
			}
				

			}
			HAL_UART_Receive_DMA(&huart4,uart_left,3);  //开启DMA接收中断	

    }
  /* USER CODE END UART4_IRQn 1 */
}

